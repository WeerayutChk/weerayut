// CONTACT FORM SUBMISSION (API INTEGRATION)
// ==========================================
// วิธีเชื่อมต่อฟอร์มเข้า Gmail ด้วย Formspree:
// 1. ไปสมัครสมาชิกที่เว็บ https://formspree.io/ (ฟรี)
// 2. สร้าง New Form แล้วก๊อปปี้ลิงก์ Endpoint URL มาใส่ในตัวแปรด้านล่างนี้
// 3. ตัวอย่าง: const FORMSPREE_ENDPOINT = "https://formspree.io/f/xyzabqwe";
const FORMSPREE_ENDPOINT = "https://formspree.io/f/mrevqjjz";

async function handleContactSubmit(event) {
    event.preventDefault();

    const submitBtn = document.getElementById("submit-btn");
    const successMsg = document.getElementById("contact-success-msg");
    const nameVal = document.getElementById("form-name").value;
    const emailVal = document.getElementById("form-email").value;
    const msgVal = document.getElementById("form-message").value;

    submitBtn.disabled = true;
    submitBtn.innerHTML = `<i class="fa-solid fa-spinner fa-spin"></i> SUBMITTING...`;

    try {
        if (FORMSPREE_ENDPOINT) {
            // ส่งข้อมูลไปยัง Form API จริง
            const response = await fetch(FORMSPREE_ENDPOINT, {
                method: 'POST',
                headers: {
                    'Content-Type': 'application/json',
                    'Accept': 'application/json'
                },
                body: JSON.stringify({
                    name: nameVal,
                    email: emailVal,
                    message: msgVal
                })
            });

            if (!response.ok) {
                throw new Error("Network response was not ok");
            }
        } else {
            // จำลองการดีเลย์ส่งข้อมูลเหมือนเดิม หากยังไม่ได้กรอก URL (เพื่อ Demo)
            await new Promise(resolve => setTimeout(resolve, 1500));
        }

        submitBtn.innerHTML = `<i class="fa-solid fa-check"></i> SENT`;
        successMsg.classList.remove("hidden");

        // Print transaction block in Terminal console if open
        appendTerminalLine(`&nbsp;`);
        appendTerminalLine(`<span class="text-green">[SYSTEM SUCCESS]</span> Message received from Contact Form:`);
        appendTerminalLine(`  Sender Name: ${nameVal}`);
        appendTerminalLine(`  Email address: ${emailVal}`);
        appendTerminalLine(`  Message summary: "${msgVal.substring(0, 40)}${msgVal.length > 40 ? '...' : ''}"`);

        // Reset form inputs
        document.getElementById("contact-form").reset();

        // Enable back button after 3s
        setTimeout(() => {
            submitBtn.disabled = false;
            submitBtn.innerHTML = `<i class="fa-solid fa-terminal"></i> ${translations[currentLanguage]["contact-btn-submit"]}`;
        }, 3000);

    } catch (error) {
        console.error("Form submission error:", error);
        submitBtn.innerHTML = `<i class="fa-solid fa-triangle-exclamation"></i> ERROR`;
        submitBtn.disabled = false;
        alert("เกิดข้อผิดพลาดในการส่งข้อความ กรุณาลองใหม่อีกครั้งครับ");
    }
}

