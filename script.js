function openGallery(projectKey) {
    activeProject = projectsData.find(p => p.key === projectKey);
    if (!activeProject) return;

    activeImageIndex = 0;
    lightboxProjectTitle.textContent = currentLanguage === "th" ? activeProject.title_th : activeProject.title_en;

    // Create Indicator Dots
    lightboxDots.innerHTML = "";
    activeProject.images.forEach((img, idx) => {
        const dot = document.createElement("button");
        dot.className = `lightbox-dot ${idx === 0 ? 'active' : ''}`;
        dot.setAttribute("aria-label", `Go to slide ${idx + 1}`);
        dot.onclick = () => {
            playSound('click');
            jumpToImage(idx);
        };
        lightboxDots.appendChild(dot);
    });

    // Display Modal
    lightboxModal.style.display = "flex";
    document.body.style.overflow = "hidden"; // Freeze scroll background

    // Load first image
    updateLightboxView();
    setupSwipeListeners();
    setupKeyboardListeners();
}

function closeGallery() {
    lightboxModal.style.display = "none";
    document.body.style.overflow = ""; // Restore scrolling
    removeSwipeListeners();
    removeKeyboardListeners();
    activeProject = null;
}

function navGallery(direction) {
    if (!activeProject) return;

    activeImageIndex += direction;

    // Circular bounds
    if (activeImageIndex >= activeProject.images.length) {
        activeImageIndex = 0;
    } else if (activeImageIndex < 0) {
        activeImageIndex = activeProject.images.length - 1;
    }

    updateLightboxView();
}

function jumpToImage(index) {
    if (!activeProject) return;
    activeImageIndex = index;
    updateLightboxView();
}

function updateLightboxView() {
    if (!activeProject) return;

    const imgUrl = activeProject.images[activeImageIndex];

    // Show spinner and hide image during change
    lightboxSpinner.style.display = "block";
    lightboxImg.classList.remove("loaded");

    // Trigger image load
    const newImg = new Image();
    newImg.src = imgUrl;
    newImg.onload = () => {
        lightboxImg.src = imgUrl;
        lightboxSpinner.style.display = "none";
        lightboxImg.classList.add("loaded");
    };

    // Update labels
    lightboxCounter.textContent = `${activeImageIndex + 1} / ${activeProject.images.length}`;

    // Update active dots
    const dots = lightboxDots.querySelectorAll(".lightbox-dot");
    dots.forEach((dot, idx) => {
        if (idx === activeImageIndex) {
            dot.classList.add("active");
            // Auto scroll dots into view if there are many dots (like 53 images)
            dot.scrollIntoView({ behavior: 'smooth', block: 'nearest', inline: 'center' });
        } else {
            dot.classList.remove("active");
        }
    });
}

// Swipe gestures for mobile carousel interaction
function setupSwipeListeners() {
    lightboxModal.addEventListener("touchstart", handleTouchStart, { passive: true });
    lightboxModal.addEventListener("touchend", handleTouchEnd, { passive: true });
}

function removeSwipeListeners() {
    lightboxModal.removeEventListener("touchstart", handleTouchStart);
    lightboxModal.removeEventListener("touchend", handleTouchEnd);
}

function handleTouchStart(e) {
    touchStartX = e.changedTouches[0].screenX;
}

function handleTouchEnd(e) {
    touchEndX = e.changedTouches[0].screenX;
    handleSwipeGesture();
}

function handleSwipeGesture() {
    const threshold = 50; // Minimum drag px
    if (touchEndX < touchStartX - threshold) {
        // Swipe Left -> Next Image
        navGallery(1);
        playSound('click');
    } else if (touchEndX > touchStartX + threshold) {
        // Swipe Right -> Prev Image
        navGallery(-1);
        playSound('click');
    }
}

// Keyboard shortcuts inside image viewer
function handleKeyPress(e) {
    if (e.key === "ArrowRight") {
        navGallery(1);
        playSound('keypress');
    } else if (e.key === "ArrowLeft") {
        navGallery(-1);
        playSound('keypress');
    } else if (e.key === "Escape") {
        closeGallery();
        playSound('click');
    }
}

function setupKeyboardListeners() {
    window.addEventListener("keydown", handleKeyPress);
}

function removeKeyboardListeners() {
    window.removeEventListener("keydown", handleKeyPress);
}

