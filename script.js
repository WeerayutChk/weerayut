function runTerminalCmd(cmd) {
    const inputField = document.getElementById("terminal-input");
    inputField.value = cmd;
    handleTerminalSubmit();
    playSound('enter');
}

function handleTerminalSubmit() {
    const inputField = document.getElementById("terminal-input");
    const cmd = inputField.value.trim().toLowerCase();

    if (cmd === "") return;

    // Add command echo to history
    appendTerminalLine(`<span class="terminal-prompt-prefix">guest@weerayut-resume:~$</span> <span class="cmd-echo">${inputField.value}</span>`);

    // Clear input
    inputField.value = "";

    // Interpret
    const langTexts = terminalTexts[currentLanguage];

    if (cmd === "clear") {
        terminalHistory.innerHTML = "";
        return;
    }

    if (langTexts[cmd]) {
        appendTerminalLine(langTexts[cmd]);
    } else {
        const errTemplate = langTexts.error.replace("{cmd}", cmd);
        appendTerminalLine(errTemplate);
    }

    // Auto Scroll terminal body to bottom
    setTimeout(() => {
        terminalBody.scrollTop = terminalBody.scrollHeight;
    }, 10);
}

function appendTerminalLine(htmlContent) {
    const line = document.createElement("div");
    line.className = "terminal-output-line";
    line.innerHTML = htmlContent;
    terminalHistory.appendChild(line);
}

